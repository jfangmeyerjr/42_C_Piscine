#include "ft_lib.h"

int	ft_checkcol(int **sudoku, int i, int y)
{
	int n;

	n = 0;
	while (n < 9)
	{
		if (sudoku[n][y] == i)
			return (0);
		n++;
	}
	return (1);
}
