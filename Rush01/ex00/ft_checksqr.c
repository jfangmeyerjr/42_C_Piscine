#include "ft_lib.h"

int	ft_checksqr(int **sudoku, int i, int x, int y)
{
	int m;
	int n;

	m = 0;
	while (m < 9)
	{
		n = 0;
		while (n < 9)
		{
			if (m / 3 == x / 3 && n / 3 == y / 3)
			{
				if (sudoku[m][n] == i)
					return (0);
			}
			n++;
		}
		m++;
	}
	return (1);
}
