void ft_putnbr(int nb)
{
	if (nb < 0)
	{
		putchar('-');
		putnbr(nb *= -1);
	}
	else if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
	{	
		ft_putchar(nb + '0');
	}
}
