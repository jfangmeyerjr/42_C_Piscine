int	ft_strcmp(char *s1, char *s2)
{
	int	difference;

	while (*s1 == *s2 && *s1 != '\0' && *s2 != '\0')
	{
		s1++;
		s2++;
	}
	difference = *(unsigned char *)s1 - *(unsigned char	*)s2;
	return (difference);
}
